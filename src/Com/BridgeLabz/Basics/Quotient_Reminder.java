package Com.BridgeLabz.Basics;

public class Quotient_Reminder {
    public static void main(String[] args) {
        int divinder= 30;
        int divisor=5;

        int quotient= divinder / divisor;
        int reminder= divinder % divisor;

        System.out.println("Quo :" +quotient);
        System.out.println("Rem :" +reminder);
    }

}
