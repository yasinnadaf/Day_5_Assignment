package Com.BridgeLabz.Basics;

public class Vowels_Consonent {
    public static void main(String[] args) {

        char ch = 'r';

        if (ch == 'a' || ch == 'i' || ch == 'o' || ch == 'u' || ch == 'e') {
            System.out.println(ch + " is vowel");
        }
        else{
            System.out.println(ch + " is consonent");
        }

    }
}
